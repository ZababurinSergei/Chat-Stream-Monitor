export type ScientificConfig = typeof SCIENTIFIC_CONFIG;

export type SpectralType = 'O' | 'B' | 'A' | 'F' | 'G' | 'K' | 'M';

export type LODLevel = 0 | 1 | 2 | 3 | 4;

// ============================================================================
// ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ДЛЯ РАБОТЫ С КОНФИГУРАЦИЕЙ
// ============================================================================

/**
 * Получить цвет спектрального типа
 */
export function getSpectralColor(spectralType: SpectralType): string {
    return SCIENTIFIC_CONFIG.ui.spectralColors[spectralType] || '#FFFFFF';
}

/**
 * Получить RGB цвет спектрального типа
 */
export function getSpectralColorRGB(spectralType: SpectralType): [number, number, number] {
    const hex = getSpectralColor(spectralType);
    const r = parseInt(hex.slice(1, 3), 16) / 255;
    const g = parseInt(hex.slice(3, 5), 16) / 255;
    const b = parseInt(hex.slice(5, 7), 16) / 255;
    return [r, g, b];
}

/**
 * Получить LOD уровень по расстоянию
 */
export function getLODLevel(distancePc: number): LODLevel {
    const thresholds = SCIENTIFIC_CONFIG.visibility.lodThresholds;
    if (distancePc <= thresholds.full3D) return 0;
    if (distancePc <= thresholds.billboard) return 1;
    if (distancePc <= thresholds.detailedPoint) return 2;
    if (distancePc <= thresholds.simplePoint) return 3;
    return 4;
}

/**
 * Получить максимальное расстояние для LOD уровня
 */
export function getMaxDistanceForLOD(level: LODLevel): number {
    const thresholds = SCIENTIFIC_CONFIG.visibility.lodThresholds;
    switch (level) {
        case 0: return thresholds.full3D;
        case 1: return thresholds.billboard;
        case 2: return thresholds.detailedPoint;
        case 3: return thresholds.simplePoint;
        default: return Infinity;
    }
}

/**
 * Обновить конфигурацию (частично)
 */
export function updateScientificConfig(updates: Partial<ScientificConfig>): void {
    Object.assign(SCIENTIFIC_CONFIG, updates);
    console.log('⚙️ ScientificConfig обновлён:', updates);
}

